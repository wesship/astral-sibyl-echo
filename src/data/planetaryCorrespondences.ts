export const planetaryHourSequence = [
  "Sun", "Venus", "Mercury", "Moon", "Saturn", "Jupiter", "Mars"
];

export const getHourlyRuler = (dayOfWeek: number, hour: number): string => {
  const dayRulers = ["Sun", "Moon", "Mars", "Mercury", "Jupiter", "Venus", "Saturn"];
  const dayRuler = dayRulers[dayOfWeek];
  const dayRulerIndex = planetaryHourSequence.indexOf(dayRuler);
  
  const hourRulerIndex = (dayRulerIndex + hour - 1) % 7;
  return planetaryHourSequence[hourRulerIndex];
};